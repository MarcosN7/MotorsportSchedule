function populateSchedule(category) {
    const scheduleContainer = document.getElementById('schedule-container');
    scheduleContainer.innerHTML = ''; // Clear previous schedule
  
    const schedule = scheduleData[category];
  
    if (!schedule || schedule.length === 0) {
      scheduleContainer.innerHTML = '<p>No events found for this category.</p>';
      return;
    }
  
    schedule.forEach(item => {
      const scheduleItem = document.createElement('div');
      scheduleItem.classList.add('schedule-item');
  
      const logo = document.createElement('img');
  
      // Set the logo based on the category
      switch (category) {
        case 'motogp':
          logo.src = 'motogp.png';
          break;
        case 'f1':
          logo.src = 'f1.png';
          break;
        case 'nascar':
          logo.src = 'NASCAR.png';
          break;
        // Add more cases for other categories as needed
        default:
          logo.src = 'f1.png';
          break;
      }
  
      // You can also set additional attributes or styles for the logo if needed
      scheduleItem.appendChild(logo);
  
      const eventDetails = document.createElement('div');
      eventDetails.classList.add('event-details');
      eventDetails.innerHTML = `
        <strong>Date:</strong> ${item.date}<br>
        <strong>Event:</strong> ${item.event}<br>
        <strong>Time:</strong> ${item.time}<br>
        <strong>Network:</strong> ${item.network}
      `;
  
      scheduleItem.appendChild(eventDetails);
      scheduleContainer.appendChild(scheduleItem);
    });
  }
  
  
  // Event listener for category selection change
  const categorySelect = document.getElementById('category-select');
  categorySelect.addEventListener('change', function() {
    const selectedCategory = this.value;
    populateSchedule(selectedCategory);
  });
  
  // Initial population of schedule based on default category
  const defaultCategory = categorySelect.value;
  populateSchedule(defaultCategory);
  
